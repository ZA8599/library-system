    private void returnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnBookActionPerformed
       int bIndex =0;
        String bookTitle;    
        
        int selectedIndex = 0;
        Books book;
        for(int i = 0; i<borLib.size(); i++){
            if(borLib.get(i).getLibrarianID().equals(id.getText())){
                selectedIndex = i;
                break;
            }
        }
        bookTitle = jTextField1.getText();
        
        for(int i=0; i<books.size(); i++){
            
            if(books.get(i).getTitle().equals(bookTitle)){
                bIndex = i;
            }
        }
        book = books.get(bIndex);
        book.Return(book.getQty());
        books.get(bIndex).setQty(book.getQty());   
        
        borLib.get(selectedIndex).setBook(book);
        borLib.get(selectedIndex).setBorrowDate(jTextField2.getText());
        borLib.get(selectedIndex).setReturnDate(jTextField3.getText());
        borLib.get(selectedIndex).setDayLate(jTextField4.getText());
//        borLib.get(selectedIndex).getBook().Return(borLib.get(selectedIndex).getBook().getQty());

        

        saveBookToFile();
        saveUserToFile2();

        mainStaff dashStaff = new mainStaff();
        dashStaff.hidden.setText(id.getText());
        dashStaff.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_returnBookActionPerformed

