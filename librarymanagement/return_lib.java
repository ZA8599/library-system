    private void submit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit1ActionPerformed
        double lateFee = 0;
        int index = 0;
        String dayLate;
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String selectDate = dateFormat.format(this.jDateChooser1.getDate());
        
        for(int i=0; i<borLib.size(); i++){
            
            if(borLib.get(i).getLibrarianID().equals(id.getText())){
                index = i;
            }
        }
        lateFee = borLib.get(index).calculateLateFee(borLib.get(index).getBorrowDate(), selectDate);
        jTextField1.setText(borLib.get(index).getBook().getTitle());
        latefee.setText(String.valueOf(lateFee));
        jTextField2.setText(borLib.get(index).getBorrowDate());
        jTextField3.setText(selectDate);
        dayLate = String.valueOf(borLib.get(index).getDayLate());
        
        jTextField4.setText(dayLate);
    }//GEN-LAST:event_submit1ActionPerformed

